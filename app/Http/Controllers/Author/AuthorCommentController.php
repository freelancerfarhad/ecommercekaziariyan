    {
        // comment::findOrFail($delete)->delete();
        $comments=comment::findOrFail($delete);
        if($comments->post->user->id==Auth::id()){
            $comments->delete();
            Toastr::success('Comment Deleted Successfully ! :)' ,'Success');
        }else{
            Toastr::error('You are not authorize to delete this comments ! :)' ,'Access Denied !');
        }
        
        return redirect()->back();
