     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $user=Auth::user();
        $posts=$user->posts;
       $popular_post=$user->posts()
        ->withCount('comments')
        ->withCount('favorite_to_user')
        ->orderBy('view_count','desc')
        ->orderBy('comments_count')
        ->orderBy('favorite_to_user_count')
        ->take(5)->get();//top  view and comment and like count
        $TotalPandingCount=$posts->where('is_approved',0)->count();//all panding post count
       $AllViewCount=$posts->sum('view_count');//all post ar view count
        return view('author.dashboard',compact('posts','popular_post','TotalPandingCount','AllViewCount'));
    }

